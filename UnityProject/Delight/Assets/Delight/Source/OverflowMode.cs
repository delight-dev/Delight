namespace Delight
{
    /// <summary>
    /// Enum indicating how items should overflow as they reach the boundaries.
    /// </summary>
    public enum OverflowMode
    {
        /// <summary>
        /// Items can exceed the boundaries.
        /// </summary>
        Overflow = 0,

        /// <summary>
        /// Items will wrap as they reach the boundaries.
        /// </summary>
        Wrap = 1
    }
}
